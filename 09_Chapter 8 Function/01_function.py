def add(a,b,c): # This function receives 3 parameters (a, b, c) and returns their sum
    return a + b + c

def sub(a,b,c):
    return a - b - c

def mul(a,b,c):
    return a * b * c

def div(a,b,c):
    return a / b / c


a = 10
b = 50
c = 30

print(add(a,b,c)) # Calling the add function with arguments (a, b, c)
print(sub(a,b,c))
print(mul(a,b,c))
print(div(a,b,c))


